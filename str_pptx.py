def generate_table(letter: str, row_length: int, num_rows: int) -> list:
    """
    Генерирует таблицу с заданной буквой, но заполняет данные по столбцам, а не по строкам.
    - Последний элемент в каждом столбце (кроме последнего) содержит 's' (например, '@cs1@').
    - Весь нижний ряд содержит 'i' (например, '@ci1@', '@ci2@', ...).
    - Последний элемент нижнего ряда — '@xis@'.

    :param letter: Буква для элементов (например, 'c', 'd', 'x').
    :param row_length: Количество элементов в каждой строке.
    :param num_rows: Количество строк.
    :return: Список списков с элементами в заданном формате.
    """
    table = [['' for _ in range(row_length)] for _ in range(num_rows)]

    counter = 1
    for i in range(row_length - 1):  # Колонки, кроме последней
        for j in range(num_rows - 1):  # Строки, кроме последней
            table[j][i] = f"@{letter}{counter}@"
            counter += 1

    # Заполнение 's' элементов в последней колонке (кроме последней строки)
    for j in range(num_rows - 1):
        table[j][-1] = f"@{letter}s{j + 1}@"

    # Заполнение последнего ряда (с 'i')
    for i in range(row_length - 1):
        table[-1][i] = f"@{letter}i{i + 1}@"
    table[-1][-1] = f"@{letter}is@"  # Последний элемент

    return table
